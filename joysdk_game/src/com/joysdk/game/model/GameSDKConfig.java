package com.joysdk.game.model;


public class GameSDKConfig {

    private ChannelPropery channelPropery;
    
    private String channelProValue;
    
    private int channelConfigId;

    
    public ChannelPropery getChannelPropery() {
        return channelPropery;
    }


    
    public void setChannelPropery(ChannelPropery channelPropery) {
        this.channelPropery=channelPropery;
    }


    public String getChannelProValue() {
        return channelProValue;
    }

    
    public void setChannelProValue(String channelProValue) {
        this.channelProValue=channelProValue;
    }

    
    public int getChannelConfigId() {
        return channelConfigId;
    }

    
    public void setChannelConfigId(int channelConfigId) {
        this.channelConfigId=channelConfigId;
    }
    
    
 
    
    
}
